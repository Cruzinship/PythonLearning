import math
#This only works with Integers and Floats type variables or values
pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) #rounds to the nearest uh...thing
print(math.ceil(pi)) #rounds up
print(math.floor(pi)) #rounds down
print(abs(pi)) #Absouloute value. This will calculate how far a number is from zero
print(pow(pi,2)) #Power. Will multiply anything by itself depedning on the number we state next to it after the comma
print(math.sqrt(420), math.sqrt(pi)) #Takes the sqaure root
print(max(x,y,z)) # Find the max value
print(min(x,y,z)) # Find the min value

